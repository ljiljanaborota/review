public class ForLoop {
    public static void main(String[] args) {
        //
        for (int i = 1; i <=5 ; i++) {
            System.out.println("hello " + i);
        }
        System.out.println("===================");
        for (int a = 0; a < 8; a+=4) {
            System.out.println("Bye");
        }
        for (int i = 2; i > 10; i++) {
            System.out.println(i);
        }
        System.out.println("-----------");
        for (int j = 3; j <10; j--) {
            System.out.println(j);
        }


    }
}
